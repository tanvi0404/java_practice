package com.tanvi.Day39;

import java.util.*;
public class RectangularPattern {
    public static void main(String[] args) {
        int b,i;
        Scanner sc = new Scanner(System.in);
        b = sc.nextInt();
        for(i=1;i<=b;i++){
            for(int j=1;j<=b;j++){
                System.out.print("*");
            }
            System.out.print("\n");
    }
    }
}
